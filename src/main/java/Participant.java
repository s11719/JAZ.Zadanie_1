
public class Participant {

	String name;
	String surname;
	String email;
	String company;
	String origin;
	String position;
	
	Participant (String name, String surname, String email, String company, String origin, String position) 
	{
		this.name = name;
		this.surname = surname;
		this.email = email;
		this.company = company;
		this.origin = origin;
		this.position = position;
	}

}
